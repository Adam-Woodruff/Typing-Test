quoteInputElement.addEventListener('input', () => {
  if (startInput == true) {
    startTimer()
    startInput = false
  }
  const arrayQuote = quoteDisplayElement.querySelectorAll('span')
  const arrayValue = quoteInputElement.value.split('')

  let correct = true
  arrayQuote.forEach((characterSpan, index) => {
    const character = arrayValue[index]
    if (character == null) {
      characterSpan.classList.remove('correct')
      characterSpan.classList.remove('incorrect')
      correct = false
    } else if (character === characterSpan.innerText) {
      characterSpan.classList.add('correct')
      characterSpan.classList.remove('incorrect')
    } else {
      characterSpan.classList.remove('correct')
      characterSpan.classList.add('incorrect')
      // correct = false
    }
  })
  if (correct) {
    calc()
    startInput = true
    getRandomString()
  }
})

async function getRandomString() {
  var i;
  var random_string = "";
  for (i = 0; i < txtlength - 1; i++) {
    var random_string = random_string.concat(Words[Math.floor(Math.random() * Words.length)], " ")
  }
  var random_string = random_string.concat(Words[Math.floor(Math.random() * Words.length)])
  // so there is no space after the text
  quoteDisplayElement.innerHTML = ''
  random_string.split('').forEach(character => {
    const characterSpan = document.createElement('span')
    characterSpan.innerText = character
    quoteDisplayElement.appendChild(characterSpan)
  })
  quoteInputElement.value = null

  console.log(random_string);
}

let startTime
function startTimer() {
  startTime = new Date()
}

function getTimerTime() {
  return Math.ceil((new Date() - startTime) / 1000)
}

function calc() {
  var errors = 0
  var count = 0

  const arrayQuote = quoteDisplayElement.querySelectorAll('span')
  const arrayValue = quoteInputElement.value.split('')
  arrayQuote.forEach((characterSpan, index) => {
    count += 1
    const character = arrayValue[index]
    if (character !== characterSpan.innerText) {
      errors += 1
    }
  })
  var WordsPerMin = Math.ceil((((txtlength * 6) - errors) / 6) / (getTimerTime() / 60))
  var Accuracy = Math.floor(100 - (errors / count) * 100)
  WPM.innerHTML = `<p class="WPM">${WordsPerMin} WPM</p>`
  ACC.innerHTML = `<p class="ACC">${Accuracy}%</p>`
}

